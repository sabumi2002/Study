package ch04;

public class BinaryTest {

	public static void main(String[] args) {
		int num = 10;
		int bNum = 0B1010;	//binary(0B)
		int oNum = 012;		//8진수(0)
		int xNum = 0XA;		//16진수(0x)
		
		System.out.println(num);
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(xNum);
		 
	}

}
